const TIMEDTEXT_CAPTURE_TIMEOUT_MS = 5000;
const TIMEDTEXT_CAPTURE_CLICK_DELAY_MS = 250;

if (typeof chrome !== "undefined" && chrome.runtime?.onMessage) {
  chrome.runtime.onMessage.addListener(handleYouTubeTimedtextCaptureMessage);
}

function handleYouTubeTimedtextCaptureMessage(message, _sender, sendResponse) {
  if (message?.type !== "captureYouTubeTimedtextTranscript") {
    return false;
  }

  captureYouTubeTimedtextTranscript({
    preferredLanguage: message.preferredLanguage
  })
    .then((result) => sendResponse(result))
    .catch((error) => sendResponse({
      ok: false,
      error: error?.message || String(error)
    }));

  return true;
}

async function captureYouTubeTimedtextTranscript(options = {}) {
  const button = findSubtitleButton();
  if (!button) {
    return {
      ok: false,
      error: "Could not find the YouTube captions button in this tab."
    };
  }

  if (button.disabled || button.getAttribute?.("aria-disabled") === "true") {
    return {
      ok: false,
      error: "YouTube captions are disabled for this video."
    };
  }

  const timedtextWait = waitForTimedtextUrl(TIMEDTEXT_CAPTURE_TIMEOUT_MS);

  try {
    await triggerSubtitleRequest(button);
  } catch (error) {
    timedtextWait.cancel();
    return {
      ok: false,
      error: error?.message || String(error)
    };
  }

  let url = "";
  try {
    url = await timedtextWait.promise;
  } catch (error) {
    return {
      ok: false,
      error: error?.message || String(error)
    };
  }

  return await fetchTimedtextTranscript(url, options);
}

function findSubtitleButton() {
  return document.querySelector(".ytp-subtitles-button") ||
    document.querySelector("button.ytp-subtitles-button");
}

async function triggerSubtitleRequest(button) {
  if (typeof button.click !== "function") {
    throw new Error("The YouTube captions button is not clickable.");
  }

  button.click();
  await delay(TIMEDTEXT_CAPTURE_CLICK_DELAY_MS);
  button.click();
}

function waitForTimedtextUrl(timeoutMs) {
  let listener = null;
  let timeout = null;

  const cleanup = () => {
    if (listener && chrome.runtime?.onMessage) {
      chrome.runtime.onMessage.removeListener(listener);
    }
    if (timeout) {
      clearTimeout(timeout);
    }
    listener = null;
    timeout = null;
  };

  const promise = new Promise((resolve, reject) => {
    listener = (message) => {
      if (message?.type !== "youtubeTimedtextUrl" || !isTimedtextUrl(message.url)) {
        return;
      }

      const url = message.url;
      cleanup();
      resolve(url);
    };

    chrome.runtime.onMessage.addListener(listener);
    timeout = setTimeout(() => {
      cleanup();
      reject(new Error("Timed out waiting for YouTube's timedtext caption request."));
    }, timeoutMs);
  });

  return {
    promise,
    cancel: cleanup
  };
}

async function fetchTimedtextTranscript(url, options = {}) {
  let response;
  try {
    response = await fetch(url, { credentials: "include" });
  } catch (error) {
    return {
      ok: false,
      error: `Could not fetch YouTube's timedtext captions: ${error?.message || String(error)}`
    };
  }

  if (!response.ok) {
    return {
      ok: false,
      error: `YouTube timedtext caption request failed (${response.status}).`
    };
  }

  const transcript = parseTimedtextResponse(await response.text(), getTimedtextFormat(url));
  if (!transcript.trim()) {
    return {
      ok: false,
      error: "YouTube returned timedtext captions, but they did not contain readable text."
    };
  }

  return {
    ok: true,
    transcript,
    title: getVideoTitle(),
    channel: getChannelName(),
    duration: getVideoDuration(),
    meta: {
      label: "YouTube player captions",
      languageCode: getTimedtextLanguage(url) || options.preferredLanguage || "",
      isAutoGenerated: getTimedtextKind(url) === "asr",
      source: "youtube-player-timedtext"
    }
  };
}

function isTimedtextUrl(value) {
  try {
    const url = new URL(String(value), "https://www.youtube.com");
    return /(^|\.)youtube\.com$/i.test(url.hostname) &&
      url.pathname === "/api/timedtext";
  } catch (_error) {
    return String(value || "").includes("/api/timedtext");
  }
}

function getTimedtextFormat(url) {
  return getTimedtextParam(url, "fmt");
}

function getTimedtextLanguage(url) {
  return getTimedtextParam(url, "lang") || getTimedtextParam(url, "tlang");
}

function getTimedtextKind(url) {
  return getTimedtextParam(url, "kind");
}

function getTimedtextParam(url, name) {
  try {
    return new URL(String(url), "https://www.youtube.com").searchParams.get(name) || "";
  } catch (_error) {
    return "";
  }
}

function parseTimedtextResponse(text, format = "") {
  const trimmed = String(text || "").trim();
  if (!trimmed) {
    return "";
  }

  if (format === "json3" || trimmed.startsWith("{")) {
    try {
      return parseJson3Captions(JSON.parse(trimmed));
    } catch (_error) {
      return "";
    }
  }

  if (format === "vtt" || trimmed.startsWith("WEBVTT")) {
    return parseVttCaptions(trimmed);
  }

  if (trimmed.includes("<text") || trimmed.includes("<p ")) {
    return parseXmlCaptions(trimmed);
  }

  return trimmed
    .split("\n")
    .map((line) => line.trim())
    .filter(Boolean)
    .join("\n");
}

function parseJson3Captions(captionJson) {
  const events = Array.isArray(captionJson?.events) ? captionJson.events : [];
  const lines = [];

  for (const event of events) {
    if (!Array.isArray(event.segs)) {
      continue;
    }

    const text = event.segs
      .map((segment) => segment?.utf8 || "")
      .join("")
      .replace(/\s+/g, " ")
      .trim();

    if (text) {
      lines.push(`[${formatTimestamp(event.tStartMs || 0)}] ${text}`);
    }
  }

  return lines.join("\n");
}

function parseVttCaptions(vttText) {
  return String(vttText || "")
    .split("\n")
    .map((line) => line.trim())
    .filter((line) => {
      if (!line) return false;
      if (line === "WEBVTT") return false;
      if (/^\d+$/.test(line)) return false;
      if (line.includes("-->")) return false;
      if (line.startsWith("NOTE")) return false;
      return true;
    })
    .map(stripHtmlTags)
    .map(decodeHtmlEntities)
    .filter(Boolean)
    .join("\n");
}

function parseXmlCaptions(xmlText) {
  const lines = [];
  const textNodeRegex = /<text\b([^>]*)>([\s\S]*?)<\/text>/g;
  const paragraphRegex = /<p\b([^>]*)>([\s\S]*?)<\/p>/g;

  collectXmlCaptionLines(xmlText, textNodeRegex, "start", lines);
  if (!lines.length) {
    collectXmlCaptionLines(xmlText, paragraphRegex, "t", lines);
  }

  return lines.join("\n");
}

function collectXmlCaptionLines(xmlText, regex, timeAttribute, lines) {
  let match = regex.exec(xmlText);
  while (match) {
    const attributes = match[1] || "";
    const rawText = match[2] || "";
    const text = decodeHtmlEntities(stripHtmlTags(rawText).replace(/\s+/g, " ").trim());
    const timestamp = getXmlTimestamp(attributes, timeAttribute);

    if (text) {
      lines.push(timestamp ? `[${timestamp}] ${text}` : text);
    }

    match = regex.exec(xmlText);
  }
}

function getXmlTimestamp(attributes, attributeName) {
  const match = attributes.match(new RegExp(`${attributeName}="([^"]+)"`));
  if (!match) {
    return "";
  }

  const value = Number(match[1]);
  if (!Number.isFinite(value)) {
    return "";
  }

  return formatTimestamp(attributeName === "start" ? value * 1000 : value);
}

function stripHtmlTags(value) {
  return String(value || "").replace(/<[^>]+>/g, "").trim();
}

function decodeHtmlEntities(value) {
  return String(value || "").replace(/&(#x?[0-9a-fA-F]+|[a-zA-Z]+);/g, (entity, code) => {
    if (code[0] === "#") {
      const isHex = code[1]?.toLowerCase() === "x";
      const numberText = isHex ? code.slice(2) : code.slice(1);
      const number = Number.parseInt(numberText, isHex ? 16 : 10);
      return Number.isFinite(number) ? String.fromCodePoint(number) : entity;
    }

    const namedEntities = {
      amp: "&",
      lt: "<",
      gt: ">",
      quot: "\"",
      apos: "'",
      nbsp: " "
    };

    return namedEntities[code] || entity;
  });
}

function getVideoTitle() {
  return textFromSelector("ytd-watch-metadata h1") ||
    textFromSelector("h1.ytd-watch-metadata") ||
    document.title.replace(/\s+-\s+YouTube$/, "").trim();
}

function getChannelName() {
  return textFromSelector("ytd-watch-metadata ytd-channel-name a") ||
    textFromSelector("ytd-watch-metadata ytd-channel-name") ||
    "";
}

function getVideoDuration() {
  const video = document.querySelector("video");
  const duration = Number(video?.duration);
  if (!Number.isFinite(duration) || duration <= 0) {
    return "";
  }

  return formatTimestamp(duration * 1000);
}

function textFromSelector(selector) {
  const node = document.querySelector(selector);
  return String(node?.textContent || "").trim();
}

function formatTimestamp(milliseconds) {
  const totalSeconds = Math.max(0, Math.floor(milliseconds / 1000));
  const hours = Math.floor(totalSeconds / 3600);
  const minutes = Math.floor((totalSeconds % 3600) / 60);
  const seconds = totalSeconds % 60;

  if (hours > 0) {
    return `${hours}:${String(minutes).padStart(2, "0")}:${String(seconds).padStart(2, "0")}`;
  }

  return `${minutes}:${String(seconds).padStart(2, "0")}`;
}

function delay(milliseconds) {
  return new Promise((resolve) => setTimeout(resolve, milliseconds));
}
